// Using Static Method to Add Two Numbers with Calculator Class.
class Calculator {
    static add(num1, num2) {
      return num1 + num2;
    }
  }
  
  // Call the static add method on the Calculator class
  console.log(Calculator.add(3, 5)); 
  
//   In this code, we define a 'Calculator' class with a static method called 'add'. The 'add' method takes two numbers as arguments and returns their sum.